package com.ibm.txtcal.DTO;

public class IncomeInfo {
	private String assYear;
	private String payerType;
	private int salIncome;
	private int deduction;
	public String getAssYear() {
		return assYear;
	}
	public void setAssYear(String assYear) {
		this.assYear = assYear;
	}
	public String getPayerType() {
		return payerType;
	}
	public void setPayerType(String payerType) {
		this.payerType = payerType;
	}
	public int getSalIncome() {
		return salIncome;
	}
	public void setSalIncome(int salIncome) {
		this.salIncome = salIncome;
	}
	public int getDeduction() {
		return deduction;
	}
	public void setDeduction(int deduction) {
		this.deduction = deduction;
	}
	
	

}
